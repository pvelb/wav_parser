int wav2csv(WavHeaderTypeDef* file, char* csv){
	if(file->chNumb==1)
	{
		if(file->bitsPerSample==8)
		{
			uint32_t sampleCnt=0;
			uint8_t * data = file;
			data=data+sizeof(WavHeaderTypeDef);
			csv=csv+sprintf(csv,"Left\n");
			for(sampleCnt=0;sampleCnt!=file->dataSize;sampleCnt=sampleCnt+1)
			{
				csv=csv+sprintf(csv,"%d\n", data[0]);
				data=data+1;
			}
			return 0;
		}
		else if(file->bitsPerSample==16)
		{
			uint32_t sampleCnt=0;
			uint16_t * data = file;
			data=data+(sizeof(WavHeaderTypeDef)>>1);
			csv=csv+sprintf(csv,"Left\n");
			for(sampleCnt=0;sampleCnt!=(file->dataSize>>1);sampleCnt=sampleCnt+1)
			{
				csv=csv+sprintf(csv,"%d\n", data[0]);
				data=data+1;
			}
			return 0;
		}
	}
	else if(file->chNumb==2)
	{
		if(file->bitsPerSample==8)
		{
			uint32_t sampleCnt=0;
			uint8_t * data = file;
			data=data+sizeof(WavHeaderTypeDef);
			csv=csv+sprintf(csv,"Left,Right\n");
			for(sampleCnt=0;sampleCnt!=file->dataSize;sampleCnt=sampleCnt+2)
			{
				csv=csv+sprintf(csv,"%d,%d\n", data[0], data[1]);
				data=data+2;
			}
			return 0;
		}
		else if(file->bitsPerSample==16)
		{
			uint32_t sampleCnt=0;
			uint16_t * data = file;
			data=data+(sizeof(WavHeaderTypeDef)>>1);
			csv=csv+sprintf(csv,"Left,Right\n");
			for(sampleCnt=0;sampleCnt!=(file->dataSize>>1);sampleCnt=sampleCnt+2)
			{
				csv=csv+sprintf(csv,"%d,%d\n", data[0], data[1]);
				data=data+2;
			}
			return 0;
		}
	}

	return -1;
}


